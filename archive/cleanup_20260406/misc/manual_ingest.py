import os

content = """
نظام الإثبات
الفصل الأول: أحكام عامة
المادة الأولى:
1- على المدعي أن يثبت ما يدعيه من حق، وللمدعى عليه نفيه.
2- يجب أن تكون الوقائع المراد إثباتها متعلقة بالدعوى، ومنتجة فيها، وجائزاً قبولها.

المادة الثانية:
1- تسري أحكام هذا النظام على الإثبات في المسائل المدنية والتجارية.
2- يطبق هذا النظام في حال عدم وجود نص خاص في الأنظمة الأخرى.

الفصل الثاني: شهادة الشهود
المادة الثالثة:
لا يجوز لمن علم من المحامين أو الوكلاء أو من في حكمهم عن طريق مهنته أو عمله بواقعة أو معلومات أن يفشيها ولو بعد انتهاء خدمته، ما لم يكن قد أريد بها ارتكاب جناية أو جنحة.

المادة الرابعة:
تقبل الشهادة في الوقائع التي لا تزيد قيمتها عن مائة ألف ريال، أو ما يعادلها.
"""

def main():
    if not os.path.exists('docs'):
        os.makedirs('docs')
    
    file_path = 'docs/judicial_system.txt'
    with open(file_path, 'w', encoding='utf-8') as f:
        f.write(f"المصدر: إدخال يدوي (نظام الإثبات السعودي)\n")
        f.write(content)
    
    print(f"[+] Document created manually: {file_path}")

if __name__ == "__main__":
    main()